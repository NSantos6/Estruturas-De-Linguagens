#!/usr/bin/perl

$n1 = 20;
$n2 = 10;


$calc = $n1 + $n2;
print 'Resultado de $n1 + $n2 = ' . $calc . "\n";

$calc = $n1 - $n2;
print 'Resultado de $n1 - $n2 = ' . $calc . "\n";

$calc = $n1 * $n2;
print 'Resultado de $n1 * $n2 = ' . $calc . "\n";

$calc = $n1 / $n2;
print 'Resultado de $n1 / $n2 = ' . $calc . "\n";

$calc = $n1 % $n2;
print 'Resultado de $n1 % $n2 = ' . $calc. "\n";
